public class AutoCoupon {

    /**
     * isopencash : 1
     * id : 18
     * money : 20.00
     * use_days : 15
     * available_money : 50.00
     * bigtypeid : 18
     * bigtypename : 钓鱼用品
     */

    private String isopencash;
    private String id;
    private String money;
    private String use_days;
    private String available_money;
    private String bigtypeid;
    private String bigtypename;

    public String getIsopencash() {
        return isopencash;
    }

    public void setIsopencash(String isopencash) {
        this.isopencash = isopencash;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMoney() {
        return money;
    }

    public void setMoney(String money) {
        this.money = money;
    }

    public String getUse_days() {
        return use_days;
    }

    public void setUse_days(String use_days) {
        this.use_days = use_days;
    }

    public String getAvailable_money() {
        return available_money;
    }

    public void setAvailable_money(String available_money) {
        this.available_money = available_money;
    }

    public String getBigtypeid() {
        return bigtypeid;
    }

    public void setBigtypeid(String bigtypeid) {
        this.bigtypeid = bigtypeid;
    }

    public String getBigtypename() {
        return bigtypename;
    }

    public void setBigtypename(String bigtypename) {
        this.bigtypename = bigtypename;
    }
}
